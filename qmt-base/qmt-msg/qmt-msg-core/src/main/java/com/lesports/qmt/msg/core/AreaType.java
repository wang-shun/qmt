public enum AreaType {
    ALL(0),
    CN(1),
    HK(2),
    US(3);

    private int value;

    AreaType(int value) {
        this.setValue(value);
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public static void main(String[] args) {
        System.out.println(AreaType.valueOf("CN").getValue());
    }
}
