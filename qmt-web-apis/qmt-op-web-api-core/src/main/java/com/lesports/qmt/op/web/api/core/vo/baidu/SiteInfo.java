public class SiteInfo {
    private String siteName;
    private String siteUrl;
    private String siteLogo;


    public String getSiteName() {
        return siteName;
    }

    public void setSiteName(String siteName) {
        this.siteName = siteName;
    }

    public String getSiteUrl() {
        return siteUrl;
    }

    public void setSiteUrl(String siteUrl) {
        this.siteUrl = siteUrl;
    }

    public String getSiteLogo() {
        return siteLogo;
    }

    public void setSiteLogo(String siteLogo) {
        this.siteLogo = siteLogo;
    }

    public static SiteInfo createSiteInfo() {
        SiteInfo siteInfo = new SiteInfo();
        siteInfo.setSiteName("乐视体育");
        siteInfo.setSiteLogo("http://i2.letvimg.com/lc01_iscms/201509/06/12/06/658ce39cded64e22b8452ef1d13a1524.png");
        siteInfo.setSiteUrl("http://sports.letv.com/");
        return siteInfo;
    }

}
