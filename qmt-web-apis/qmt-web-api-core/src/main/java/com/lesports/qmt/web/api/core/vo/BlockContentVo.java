package com.lesports.qmt.web.api.core.vo;

/**
 * Created by ruiyuansheng on 2016/12/29.
 */
public class BlockContentVo {

        private Long id;//版块内容ID
        private String title;//版块内容标题
        private String subTitle;//副标题（看点）
        private String tvPic;
        private String pic1;//TV会员领取引导图专用
        private Integer type;
        private String content;
        private String startTime;
        private int status;
        private int cornerMark;//1:vip,2:原创，3：专题，4：图集，5，回放，6：集锦
        private Long ctime;//创建时间
        private Long videoId;

        public String getTvPic() {
            return tvPic;
        }

        public void setTvPic(String tvPic) {
            this.tvPic = tvPic;
        }

        public int getCornerMark() {
            return cornerMark;
        }

        public void setCornerMark(int cornerMark) {
            this.cornerMark = cornerMark;
        }

        public Long getCtime() {
            return ctime;
        }

        public void setCtime(Long ctime) {
            this.ctime = ctime;
        }

        public Long getVideoId() {
            return videoId;
        }

        public void setVideoId(Long videoId) {
            this.videoId = videoId;
        }

        public String getStartTime() {
            return startTime;
        }

        public void setStartTime(String startTime) {
            this.startTime = startTime;
        }

        public int getStatus() {
            return status;
        }

        public void setStatus(int status) {
            this.status = status;
        }


        public String getPic1() {
            return pic1;
        }

        public void setPic1(String pic1) {
            this.pic1 = pic1;
        }

        public Long getId() {
            return id;
        }

        public void setId(Long id) {
            this.id = id;
        }

        public String getTitle() {
            return title;
        }

        public void setTitle(String title) {
            this.title = title;
        }

        public String getSubTitle() {
            return subTitle;
        }

        public void setSubTitle(String subTitle) {
            this.subTitle = subTitle;
        }


        public Integer getType() {
            return type;
        }

        public void setType(Integer type) {
            this.type = type;
        }



        public String getContent() {
            return content;
        }

        public void setContent(String content) {
            this.content = content;
        }

        @Override
        public String toString() {
            final StringBuffer sb = new StringBuffer("BlockContent{");
            sb.append("id=").append(id);
            sb.append(", title='").append(title).append('\'');
            sb.append(", subTitle='").append(subTitle).append('\'');
            sb.append(", tvPic='").append(tvPic).append('\'');
            sb.append(", type=").append(type);
            sb.append(", content=").append(content);
            sb.append(", cornerMark=").append(cornerMark);
            sb.append(", status=").append(status);
            sb.append(", startTime=").append(startTime);
            sb.append('}');
            return sb.toString();
        }
}
