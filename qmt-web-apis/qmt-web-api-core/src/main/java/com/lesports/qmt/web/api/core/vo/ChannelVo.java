public class ChannelVo {

    //频道id
    private Long channelId;
    //频道名称
    private String channelName;
    //频道logo
    private String postOrigin;
    //当前播放的节目名
    private String currentEpisodeTitle;
    private int isDrm;

    private Integer order;
    private Boolean showCornerMark;//是否显示角标，true显示，false隐藏
    private Integer isPay;

    public Integer getOrder() {
        return order;
    }

    public void setOrder(Integer order) {
        this.order = order;
    }

    public Long getChannelId() {
        return channelId;
    }

    public void setChannelId(Long channelId) {
        this.channelId = channelId;
    }

    public String getChannelName() {
        return channelName;
    }

    public void setChannelName(String channelName) {
        this.channelName = channelName;
    }

    public String getPostOrigin() {
        return postOrigin;
    }

    public void setPostOrigin(String postOrigin) {
        this.postOrigin = postOrigin;
    }

    public String getCurrentEpisodeTitle() {
        return currentEpisodeTitle;
    }

    public void setCurrentEpisodeTitle(String currentEpisodeTitle) {
        this.currentEpisodeTitle = currentEpisodeTitle;
    }

    public int getIsDrm() {
        return isDrm;
    }

    public void setIsDrm(int isDrm) {
        this.isDrm = isDrm;
    }

    public Boolean getShowCornerMark() {
        return showCornerMark;
    }

    public void setShowCornerMark(Boolean showCornerMark) {
        this.showCornerMark = showCornerMark;
    }

    public Integer getIsPay() {
        return isPay;
    }

    public void setIsPay(Integer isPay) {
        this.isPay = isPay;
    }
}
