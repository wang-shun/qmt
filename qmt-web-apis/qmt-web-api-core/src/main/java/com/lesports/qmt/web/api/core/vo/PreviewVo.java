public class PreviewVo {

    private Integer needPreview;//是否需要试看，1是需要，0是不需要

    private Integer previewTime;//如果需要试看的话，试看时长，单位秒

    public Integer getNeedPreview() {
        return needPreview;
    }

    public void setNeedPreview(Integer needPreview) {
        this.needPreview = needPreview;
    }

    public Integer getPreviewTime() {
        return previewTime;
    }

    public void setPreviewTime(Integer previewTime) {
        this.previewTime = previewTime;
    }
}
